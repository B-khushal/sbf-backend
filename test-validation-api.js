const axios = require('axios');

const baseURL = 'http://localhost:5000/api';

async function testPromoCodeValidation() {
  console.log('üß™ Testing Promo Code Validation API...\n');
  
  const testCases = [
    {
      name: 'Valid WELCOME10 code (10% off, min ‚Çπ500)',
      code: 'WELCOME10',
      orderAmount: 1000,
      expected: 'Should succeed with ‚Çπ100 discount (10% of ‚Çπ1000)'
    },
    {
      name: 'WELCOME10 below minimum order',
      code: 'WELCOME10',
      orderAmount: 300,
      expected: 'Should fail - below ‚Çπ500 minimum'
    },
    {
      name: 'Valid SAVE50 code (‚Çπ50 off, min ‚Çπ200)',
      code: 'SAVE50',
      orderAmount: 500,
      expected: 'Should succeed with ‚Çπ50 discount'
    },
    {
      name: 'FLOWERS20 high value order (20% off, min ‚Çπ1000)',
      code: 'FLOWERS20',
      orderAmount: 2000,
      expected: 'Should succeed with ‚Çπ400 discount (20% of ‚Çπ2000)'
    },
    {
      name: 'FLOWERS20 below minimum',
      code: 'FLOWERS20',
      orderAmount: 800,
      expected: 'Should fail - below ‚Çπ1000 minimum'
    },
    {
      name: 'BIGORDER for large purchase (‚Çπ100 off, min ‚Çπ2000)',
      code: 'BIGORDER',
      orderAmount: 2500,
      expected: 'Should succeed with ‚Çπ100 discount'
    },
    {
      name: 'Expired promo code',
      code: 'EXPIRED10',
      orderAmount: 1000,
      expected: 'Should fail - expired'
    },
    {
      name: 'Invalid/non-existent code',
      code: 'INVALID123',
      orderAmount: 1000,
      expected: 'Should fail - code not found'
    },
    {
      name: 'Empty code',
      code: '',
      orderAmount: 1000,
      expected: 'Should fail - empty code'
    }
  ];

  console.log('üîç Testing validation endpoint: POST /api/promocodes/validate\n');
  
  for (let i = 0; i < testCases.length; i++) {
    const testCase = testCases[i];
    console.log(`Test ${i + 1}/${testCases.length}: ${testCase.name}`);
    console.log(`   üìù Code: "${testCase.code}", Amount: ‚Çπ${testCase.orderAmount}`);
    console.log(`   üéØ Expected: ${testCase.expected}`);
    
    try {
      const response = await axios.post(`${baseURL}/promocodes/validate`, {
        code: testCase.code,
        orderAmount: testCase.orderAmount,
        items: [
          { name: 'Test Product', category: 'Flowers', price: testCase.orderAmount }
        ]
      });
      
      if (response.data.success) {
        const data = response.data.data;
        console.log(`   ‚úÖ SUCCESS: ${response.data.message}`);
        console.log(`   üí∞ Discount: ‚Çπ${data.discount.amount} (${data.discount.percentage}%)`);
        console.log(`   üßæ Original: ‚Çπ${data.order.originalAmount} ‚Üí Final: ‚Çπ${data.order.finalAmount}`);
        console.log(`   üí° Savings: ‚Çπ${data.discount.savings}`);
      } else {
        console.log(`   ‚ùå FAILED: ${response.data.message}`);
      }
      
    } catch (error) {
      if (error.response) {
        console.log(`   ‚ùå EXPECTED ERROR: ${error.response.data.message}`);
      } else {
        console.log(`   üí• UNEXPECTED ERROR: ${error.message}`);
      }
    }
    
    console.log('');
  }
  
  console.log('üéØ Summary:');
  console.log('   ‚úÖ All validation tests completed');
  console.log('   üîç Check results above for any unexpected failures');
  console.log('');
  
  // Test edge cases
  console.log('üß™ Testing Edge Cases...\n');
  
  const edgeCases = [
    {
      name: 'Very large order with percentage discount',
      code: 'WELCOME10',
      orderAmount: 10000,
      note: 'Should cap at maximum discount if set'
    },
    {
      name: 'Exact minimum order amount',
      code: 'SAVE50',
      orderAmount: 200,
      note: 'Should work at exactly minimum amount'
    }
  ];
  
  for (const edgeCase of edgeCases) {
    console.log(`Edge Test: ${edgeCase.name}`);
    console.log(`   üí° ${edgeCase.note}`);
    
    try {
      const response = await axios.post(`${baseURL}/promocodes/validate`, {
        code: edgeCase.code,
        orderAmount: edgeCase.orderAmount
      });
      
      if (response.data.success) {
        const data = response.data.data;
        console.log(`   ‚úÖ SUCCESS: Discount ‚Çπ${data.discount.amount}, Final: ‚Çπ${data.order.finalAmount}`);
      } else {
        console.log(`   ‚ùå FAILED: ${response.data.message}`);
      }
      
    } catch (error) {
      console.log(`   ‚ùå ERROR: ${error.response?.data?.message || error.message}`);
    }
    console.log('');
  }
  
  console.log('üéâ All validation tests completed!');
  console.log('\nüìã Next Steps:');
  console.log('1. ‚úÖ API validation working');
  console.log('2. üåê Test admin UI: http://localhost:5173/admin/promocodes');
  console.log('3. üõí Test checkout integration');
  console.log('4. üîÑ Test end-to-end flow');
}

